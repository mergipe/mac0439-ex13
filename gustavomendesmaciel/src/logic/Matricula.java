package logic;

public class Matricula
{

    private Aluno aluno;
    private Curso curso;

    public Aluno getAluno()
    {
        return this.aluno;
    }

    public Curso getCurso()
    {
        return this.curso;
    }

    public void setAluno(Aluno aluno)
    {
        this.aluno = aluno;
    }

    public void setCurso(Curso curso)
    {
        this.curso = curso;
    }
}
